package com.example.bluetreeassignment.Models;


public class Product {
    private int id;
    private String title;
    private double price;
    private String thumbnail;



    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public double getPrice() {
        return price;
    }

    public String getThumbnail() {
        return thumbnail;
    }

}